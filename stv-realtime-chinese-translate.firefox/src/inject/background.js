chrome.runtime.onMessage.addListener(function (message, sender, callback) {
    switch(message.type) {
        case 'getScreen':
            chrome.desktopCapture.chooseDesktopMedia([chrome.desktopCapture.DesktopCaptureSourceType.TAB],async function(streamid){console.log(streamid);
                callback({
                    video: {
                        mandatory: {
                            chromeMediaSource: 'desktop',
                            chromeMediaSourceId: streamid
                        }
                    }
                    
                });
                return false;
            });
            return true; // retain callback for chooseDesktopMedia result
        case 'cancelGetScreen':
            chrome.desktopCapture.cancelChooseDesktopMedia(message.request);
            message.type = 'canceledGetScreen';
            callback(message);
            return false; //dispose callback
    }
});

